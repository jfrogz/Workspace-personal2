package xpd.web.gateway.client;

public enum OperationType {
	PERSON_CREATE( 1, "/person/create"),
	PERSON_UPDATE( 2, "/person/update"),
	PERSON_FIND( 3, "/person/find");
	
	private OperationType( int code, String path) {
		this.code = code;
		this.path = path;
	}
	
	private int code;
	private String path;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
	
}
